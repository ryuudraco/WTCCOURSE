#include "libft.h"

void	ft_putnbr(int val)
{
	long	n;
	char	str[30];
	int		sign;
	int		i;

	i = 0;
	n = (long)val;
	if ((sign = n) < 0)
		n *= -1;
	str[i++] = (n % 10) + '0';
	n /= 10;
	while (n > 0)
	{
		str[i++] = (n % 10) + '0';
		n /= 10;
	}
	if (sign < 0)
		str[i++] = '-';
	while (i-- > 0)
		write(1, &str[i], 1);
}
