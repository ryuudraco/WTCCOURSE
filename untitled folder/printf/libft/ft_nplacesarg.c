#include "libft.h"

int		ft_nplacesarg(int num)
{
	int	i;

	if (num == -2147483648)
		return (0);
	i = 0;
	if (num == 0)
		return (1);
	if (num < 0)
	{
		num = -num;
		i++;
	}
	while (num)
	{
		num /= 10;
		i++;
	}
	return (i);
}
